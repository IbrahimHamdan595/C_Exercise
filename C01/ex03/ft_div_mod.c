#include <unistd.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int	main(void)
{
	int		x;
	int		y;

	ft_div_mod(10, 3, &x, &y);
	return (0);
}
